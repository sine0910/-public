    public bool name_used_limit_word_check(string name)
    {
        for (int i = 0; i < bad_word_list.Count; i++)
        {
            var badWord = bad_word_list[i].ToString();

            while (true)
            {
                if (name.Contains(badWord))
                {
                    Debug.Log("player_name: " + name + " contain badword: " + badWord);
                    return true;
                }
                else
                {
                    break;
                }
            }
        }
        return false;
    }

    public void other_use_this_name_result(byte result)
    {
        switch (result)
        {
            case 1:
                {
                    DataManager.instance.save_my_name_data(name_value);
                    complete_name_set();
                }
                break;
            case 2:
                {
                    name_value = "";
                    name_inputfield.text = "";
                    cannot_use_name.SetActive(true);
                }
                break;
            case 3:
                {
                    NetworkManager.Network_Error();
                }
                break;
        }
    }

    void complete_name_set()
    {
